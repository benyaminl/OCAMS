    public void updateTotal(){
        int total = 0;
        for (int i = 0; i < table.getRowCount(); i++) {
            int temp = Integer.parseInt(table.getValueAt(i, 4).toString());
            total += temp;
        }
        lblTotal.setText(String.valueOf(total));
    }
    
