    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        boolean cek = true;
        String kode = OCAMS.SQL.executeGetScalar("SELECT id_bahan FROM bahan where nama_bahan = '"
                +cbNama.getSelectedItem().toString()+"'");
        String tambah[] = {kode,cbNama.getSelectedItem().toString(),spJumlah.getValue().toString()
                ,"0",txtHargaBeli.getText()};
        for (int i = 0; i < table.getRowCount(); i++) {
            if(table.getValueAt(i, 0).toString().equalsIgnoreCase(kode)){
                int t = Integer.parseInt(table.getValueAt(i, 2).toString());
                t += Integer.parseInt(spJumlah.getValue().toString());
                //"ID Bahan","Nama Bahan","Jumlah","Tersisa","Harga Total"
                table.setValueAt(t, i,2);
                t = Integer.parseInt(table.getValueAt(i, 4).toString());
                t+= Integer.parseInt(txtHargaBeli.getText());
                table.setValueAt(t, i,4);
                cek = false;
            }
        }
        if(cek) table.addRow(tambah);
        updateTotal();
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        OCAMS.SQL.executeNonQuery("INSERT INTO HEADER_STOCK VALUES('"+lblTrans.getText()
                +"',CURRENT_DATE,'"+OCAMS.userYangLogin.getKdUser()+"') "
                + "on duplicate key update id_trans = id_trans");
        for (int i = 0; i < table.getRowCount(); i++) {
            OCAMS.SQL.executeNonQuery("INSERT INTO detail_stock values('"+lblTrans.getText()+"',"
                + "'"+table.getValueAt(i, 0)+"',"+spJumlah.getValue().toString()+","
                +txtHargaBeli.getText()+",0) "
                    + "on duplicate key update harga = harga"
                    + "");
        }
        String perkiraan[];
        if(cbKas.isSelected()){
            perkiraan = new String[] {"Kas,0,"+lblTotal.getText(),"Stock,"+lblTotal.getText()+",0"};
        }
        else{
            perkiraan = new String[] {"Hutang,0,"+lblTotal.getText(),"Stock,"+lblTotal.getText()+",0"};
        }
        
        OCAMS.SQL.pencatatanJurnalTransaksi(lblTrans.getText(), "Setok Barang", perkiraan, OCAMS.userYangLogin.getKdUser());
        JOptionPane.showMessageDialog(this, "Berhasil Disimpan!");
        parent.updateTable();
    }//GEN-LAST:event_jButton2ActionPerformed

